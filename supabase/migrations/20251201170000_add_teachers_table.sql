create table if not exists public.teachers (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  id_code text not null,
  normalized_id_code text not null,
  created_at timestamptz default now()
);

create unique index if not exists teachers_normalized_id_code_idx
  on public.teachers (normalized_id_code);

alter table public.teachers enable row level security;

create policy if not exists "Allow all operations on teachers"
  on public.teachers
  for all
  using (true)
  with check (true);

